//Arthmatic Operator
#include <iostream>
using namespace std;

int main() //datatype  use when which kind of value return in code.
{
	int a = 21; //int use for integer value.
	int b = 10;
	int c;

	//Addition
	c = a + b;
	cout<<"line 1 - value of c is :" <<c<< endl;  //"cout" use for print line. 

	//Subtraction
	c = a - b;
	cout<<"line 2 - value of c is :" <<c<< endl; //"endl" use for linebreak. 

	//Multiplication
	c = a * b;
	cout<<"line 3 - value of c is :" <<c<< endl;

	//Division
	c = a / b;
	cout<<"line 4 - value of c is :" <<c<< endl;

	//Modulus
	c = a % b;
	cout<<"line 5  - value of c is :" <<c<< endl;

	return 0;
}

